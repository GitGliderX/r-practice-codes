#Arithmatic operators
x<-10
y<-5

print(x+y)
print(x-y)
print(x*y)
print(x/y)
print(x^y)
print(x%%y)
print(x%/%y)

#Assignment Operator

op1<-1
op2<<-2
op3=3
4->op4
5->>op5

print(op1)
print(op2)
print(op3)
print(op4)
print(op5)

#comparison Operator

com1=100
com2=20

print(com1==com2)
print(com1>com2)
print(com1<com2)
print(com1>=com2)
print(com1<=com2)
print(com1!=com2)

